            <h3>Purchase Funding - <small>Supply Chain Funding</small></h3>
            <p>Purchase funding adalah pembiayaan atas pesanan pembelian (purchase order) dari pelanggan, untuk membiayai perusahaan yang membutuhkan dana tunai untuk memenuhi pesanan baik satu atau beberapa pesanan pelanggan.</p>
            <ul>
                <li>Jangka waktu 1, 2, 3, 6 dan 9 bulan</li>
                <li>Maksimum pembiayaan 50% x nilai purchase order atau maksimum sebesar Rp. 500.000.000,- per transaksi</li>
                <li>Agunan debitur adalah kombinasi dari: Collateral-based lending (laporan keuangan, risk scoring, relationship, asset) dengan Purchase Order (pesanan pembelian) yang telah diterima dengan baik (accepted) oleh Penjualnya</li>
                <li>Penjaminan/Asuransi Kredit dengan membayar Imbal Jasa Penjaminan (IJP) atas sejumlah pinjaman yang dijamin (jika diperlukan)</li>
                <li>Biaya bunga pinjaman dan biaya layanannya mengacu pada risk score nya</li>                
            </ul>
            <br/>
            <h4>KEUNTUNGAN </h4>
            <span style="font-weight: bold;">Bagi Perusahaan</span>
            <ul>
                <li>Meringankan beban perusahaan membayar dimuka untuk membeli bahan baku untuk produksi atau barang untuk diserahkan guna memenuhi pesanan pelanggan;</li> 
                <li>Proses pinjaman secara elektronik membuat biaya bunga dan biaya layanan menjadi lebih murah dibanding proses pinjaman secara tradisional.</li>
            </ul>
