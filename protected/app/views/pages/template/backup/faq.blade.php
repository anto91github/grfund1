					  <div class="panel-body">
						When a project is over funded backers sometimes wonder: what happens with all that “extra” money? If a creator has a funding goal of $5,000 and raises $25,000, what do they do with the $20,000 they didn’t ask for?<br/>
What the creator does in that case varies greatly depending on the project.<br/>
Most of the time what seems like “extra” money isn’t extra at all. Ten times the funding often means ten times the backers. More rewards have to be produced and distributed, and creators need that funding to do it (and sometimes some of their own money too).<br/>
Sometimes when a project is over funded it lets the creator put that money back into the project to create something better for the backers and themselves. More songs on an album, additional game elements, better materials, etc.<br/>
In other cases, over funding leads to better margins and the creator may even profit from the project.</div>
