						 Can I use any copyrighted material as part of my project?</a>
					  </h4>
					</div>
					<div class="panel-collapse collapse" id="16">
					  <div class="panel-body">
						Don’t use music, images, video, or other content that you don’t have the rights to. Using copyrighted material is almost always against the law and can lead to expensive lawsuits down the road.<br/>
When copyright disputes do arise on GoongRoyongFund (they’re rare, but they happen), we handle them in accordance with the Digital Millennium Copyright Act of 1998. To avoid unnecessary disruption, projects in question are typically removed from public view but are not immediately canceled. This keeps pledge authorizations and lines of communication intact, and leaves room for project recovery where appropriate. The procedure is outlined in detail on our Copyright and DMCA Policy page.<br/>
The easiest way to avoid copyright troubles altogether, though, is to create all the content yourself or use content that is free for public use. For example, you may be able to use some Creative Commons-licensed music — per the terms of their licenses — that’s available on Sound Cloud. Other resources worth exploring: Vimeo, Music Store,Free Music Archive and ccMixter.</div>
