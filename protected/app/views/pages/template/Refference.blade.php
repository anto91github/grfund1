    		Selamat datang di Gotong Royong Fund! Kami hadir memberi ruang untuk berbagai macam aspek kebutuhan masyarakat Indonesia melalui penggalangan dana. Mari wujudkan Indonesia yang lebih baik bersama Gotong Royong Fund!<br/><br/>
    		<b>MARI PROYEKSIKAN IDE-MU!</b><br/>
    		Memiliki ide kreatif atau ingin dapat membantu orang dekatmu? Mari kita wujudkan melalui Gotong Royong Fund! Kirimkan ide-mu secara lengkap, disertai hal-hal dibawah ini:<br/>
    		<span style="padding-left:1em">1. Tulis latar belakang dari idemu, meliputi: tujuan, perkenalan diri atau organisasi, jumlah dana yang diperlukan, dan rencana untuk proyek tersebut.<br/></span>
    		<span style="padding-left:1em">2. <i>Rewards</i> yang didapat oleh para donatur.<br/></span>
    		<span style="padding-left:1em">3. Perkembangan proyek-mu.<br/><br/></span>

    		<b>CERITAMU!</b><br/>
    		Pada laman <a href="http://bit.ly/Ceritamu">http://bit.ly/Ceritamu</a> ini kamu bisa menceritakan ide <i>project</i> yang ingin diberitakan. Apa dan berapa banyak bantuan dana yang dibutuhkan? Buat video yang menceritakan tujuan, visi dan misi dari project-mu.<br/>
    		Video yang dimaksud di sini, tidak harus video yang professional. Pastikan hal-hal berikut ada di dalam video-mu: <br/>
    		<ul>
    			<li>Perkenalan semua anggota tim dari project-mu</li>
    			<li>Ceritakan dengan <i>detail</i> latar belakang <i>project</i> dan berikan perkembangan-mu secara rutin. Contoh: <i>video footage</i>, foto-foto, ataupun penelitian yang sudah kamu buat.</li>
    		</ul>

			<br/><b><i>REWARDS</i></b><br/>
			<i>Rewards</i> dapat berupa barang atau bentuk lainnya yang kamu berikan sebagai bentuk apresiasi untuk para donatur. Kamu juga dapat menggabungkan <i>souvenir</i> dengan sesuatu lainnya, misal yang bersifat unik. <br/>
			Barang yang bisa dijadikan souvenir:
			<ul>
    			<li>Kaos dengan identitas <i>project</i>-mu</li>
    			<li>Gelas</li>
    			<li>Aksesoris</li>
    			<li>Dan lain-lain</li>
    		</ul>
    		Hitung harganya<br/>
    		Sebelum memberikan <i>rewards</i>, pastikan kamu sudah menghitung biayanya. Contoh, jika donatur menyumbang IDR 1.000.000 dia akan mendapatkan gelas. Jika dia menyumbang IDR 10.000.000, dia akan mendapatkan kaos.
    		<br/><br/>

    		<b id="funding">PENDANAAN</b><br/>
    		GRF adalah wadah untuk menemukan donatur bagi projectmu.<br/>
    		<ul>
    			<li>Buat daftar pengeluaran yang kamu butuhkan secara rinci dan ter-struktur.</li>
    			<li>Sebar luaskan <i>project</i>-mu melalui sosial media dan orang sekitar.</li>
    			<li>Tentukan tujuanmu setelah dana terkumpul.</li>
    			<li>Berikan batasan waktu untuk pengumpulan dana</li>
    		</ul>
            Fee = 5% dari total jumlah dana yang terkumpul<br/>

    		<br><b>PROMOSI</b><br/>
    		Untuk mendukung promosi ide-mu, kamu juga dapat melakukan hal-hal berikut:
    		<ul>
    			<li>Berikan pengumuman dan url <i>project</i> GRF-mu melalui <i>email</i>, sosial media, dan <i>blog</i> pribadimu.</li>
    			<li>Bergabunglah dengan komunitas yang memiliki visi dan misi yang sama.</li>
    			<li>Ajak seluruh tim-mu untuk menyebar luaskan kegiatan yang kamu lakukan.</li>
    		</ul>

    		<br><b>PERKEMBANGAN</b><br/>
    		Setelah mendapatkan dana, kamu harus memberi tahu para donatur mengenai perkembangan <i>project</i>-mu. Diantaranya:

    		<ul>
    			<li>Foto dan <i>footage</i> untuk setiap detail perkembangan</li>
    			<li>Pastikan kamu juga meminta pendapat mereka mengenai hal yang sudah kamu lakukan</li>
    			<li>Jika seluruh dana sudah terkumpul, jangan lupa untuk menghubungi para donatur secara personal dan mengundangnya untuk hadir di acara-mu.</li>
    		</ul>

    		<br><b>APRESIASI</b><br/>
    		Apresiasi adalah langkah selanjutnya yang harus kamu lakukan jika dana yang kamu butuhkan sudah terkumpul. Ini saatnya kamu memberikan <i>rewards</i> (jika disediakan) untuk para donatur. Caranya:
			<ul>
    			<li>Temukan daftar, alamat lengkap, dan data para donatur.</li>
    			<li>Tanyakan pendapat mereka mengenai <i>project</i>-mu</li>
    			<li>Pastikan seluruh donatur mendapatkannya.</li>
    		</ul>

    		<br><b>BACA SELENGKAPNYA</b><br/>
    		Selain langkah-langkah untuk membuat projectmu, GRF juga menyediakan artikel lainnya untuk menambah pengetahuan-mu.<br/>&nbsp;
