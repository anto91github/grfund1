            <h3>Invoice Financing - <small>Factoring</small></h3>
            <p>Invoice finance - factoring atau anjak-piutangadalah jenispembiayaan bagi pemasok (supplier financing)di mana perusahaanmenjualinvoice/tagihan atau akun piutangnya yang layak-kreditkepada Faktor dalam hal ini adalah Pinjaman Gotongroyong (GRLending) - Koperasi Selaras dan menerimapembayaran tunai dimuka.</p>
            <ul>
                <li>Jangka waktu pinjaman 1, 2, 3, 6 dan 9 bulan</li>
                <li>Maksimum pembiayaan 90% x nilai invoice atau maksimum sebesar Rp. 1.000.000.000,- per transaksi</li>
                <li>Agunan peminjam adalah kombinasi dari: Information-based lending (laporan keuangan, risk scoring, relationship) dengan Invoice (tagihan) yang telah diterima dengan baik (accepted)oleh Pembelinya</li>
                <li>Penjaminan/Asuransi Kredit dengan membayar Imbal Jasa Penjaminan (IJP) atas sejumlah pinjaman yang dijamin (jika diperlukan)</li>
                <li>Biaya bunga pinjaman dan biaya layanannya mengacu pada risk score nya</li>
                <li>Persyaratan lainnya:
                    <ul>
                        <li>Calon peminjam harus diundang oleh pembeli untuk bergabung dalam program pinjaman ini;</li>
                        <li>Telahmenjadi pemasokselama sedikitnya enambulandan telahsepenuhnya kompatibeldengan semuapesanan pembelian;</li>
                        <li>Telahmenyelesaikansetidaknya satupesanan pembelianper bulan(yaitu adalah pemasokregular)</li>
                    </ul>
