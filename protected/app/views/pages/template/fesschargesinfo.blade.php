                <h4>Biaya Pemodal Modal Gotongroyong</h4>
                <p>Penghimpunan Modal Gotongroyong dan penyaluran pinjaman oleh Pinjaman Gotongroyong menggunakan biaya operasi dan investasi yang rendah sehingga memberi nilai tambah di imbal-hasil bagi pemodal. Kami menerima imbal jasa hanya jika pemodal menerima hasil pembayaran sehingga penghasilan kami dari pemodal terkait langsung dengan arus kas pemodal.</p>
                <ul>
                    <li><b>Biaya layanan investasi (Investment fee)</b>
                    <p>Kami mengurus pinjaman termasuk mengurus rekening-rekening pemodal, penagihan, mengolah modal pokokdan penghasilan bunga berikut denda dari peminjam, men-distribusikan pembayaran tersebut setelah dikurangi biaya ke pemodal. Untuk itu, pemodal membayar ke kami atas biaya layanan investasi sebanyak 1% (satu persen) dari setiap pembayaran yang diterima oleh pemodal.</p>
                    </li>
                    <li><b>Biaya Penagihan (Collection fee)</b>
                    <p>Jika peminjam terlambat atau tertunda pembayaran bunga dan pelunasan pembayaran pokoknya, maka kami akan berusaha sebaik-baiknya agar pinjaman yang terlambat atau tertunda tersebut kembali ke kondisi normal. Sekarang ini kami akan membebankan biaya penagihan ke Modal Gotongroyong maksimal sebanyak 15% (lima belas persen) dari perolehan kembali (recovered) pembayaran yang telah terlambat 8 (delapan) hari kalender atau lebih tanpa proses hukum (litigasi). Namun jika terjadi proses hokum (litigasi), maka kami akan membebankan biaya penagihan ke Modal Gotongroyong maksimal sebanyak 30% (tiga puluh persen) untuk biaya pengacara dan ongkosnya. Kami tidak akan membebankan biaya penagihan jika tidak ada perolehan kembali pembayaran dari peminjam tersebut. </p>
